/**
  * @brief  Function implementing the defaultTask thread.
  * @param  argument: Not used 
  * @retval None
  */
/* USER CODE END Header_StartDefaultTask */
void StartDefaultTask(void const * argument)
{
  int sock, i = 0;
  struct sockaddr_in localhost, remotehost;
  char data[100];
  /* init code for LWIP */
  MX_LWIP_Init();

  /* Wait for IP address assignment */
  while(gnetif.ip_addr.addr == 0)
  {
	  osDelay(100);
  }
  if((sock = socket(AF_INET,SOCK_DGRAM, IPPROTO_UDP)) >= 0)
  {
	  memset(&localhost, 0, sizeof(struct sockaddr_in));
	  localhost.sin_family = AF_INET;
	  localhost.sin_port = htons(1234);
	  localhost.sin_addr.s_addr = INADDR_ANY;
	  bind(sock, (struct sockaddr *)&localhost, sizeof(struct sockaddr_in));
	  memset(&remotehost, 0, sizeof(struct sockaddr_in));
	  remotehost.sin_family = AF_INET;
	  remotehost.sin_port = htons(6666);
	  /* Broadcast... */
	  remotehost.sin_addr.s_addr = INADDR_BROADCAST;
	  /* or unicast */
	  //ip4addr_aton("192.168.0.100",(ip4_addr_t*)&remotehost.sin_addr);
  }
  /* Infinite loop */
  for(;;)
  {
	  sprintf(data, "Packet #%03d: Hello from DevBoard!", i);
	  sendto(sock, data, strlen(data), 0, (struct sockaddr *)&remotehost, sizeof(remotehost));
	  osDelay(2000);
	  i++;
	  if(i > 999) i = 0;
  }
  /* USER CODE END 5 */ 
}

